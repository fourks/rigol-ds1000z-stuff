sub subtree {
  my ($current_index, $path) = @_;

  my $long_name = $raw_table->[$current_index]->[0];
  my $yes = $raw_table->[$current_index][2];
  my $no  = $raw_table->[$current_index][3];

  if ($yes == 0xFFFF) {
    my $fullname = "$path:$long_name";
    printf "%s %s\n", check_documentation($fullname) ? " " : "!", $fullname;
  } else {
    subtree($yes, "$path:$long_name");
  }

  if ($no != 0xFFFF) {
    subtree($no, "$path");
  }
}

sub check_documentation {
  my ($fullpath) = @_;

  $fullpath =~ s/^:\*/\\*/;
  $fullpath =~ s/\?/\\?/;
  #$fullpath =~ s/:/(<n>)?:/g;
  $fullpath = "$fullpath";


  # Preprocessing:
  #  s/\[//g;
  #  s/\]//g;
  #  s/<n>//g;

  #print "Regex: $fullpath\n";
  my $out = `ack '$fullpath' MSO1000Z_DS1000Z_ProgrammingGuide_EN.preproc.txt`;
  my $outlen = length $out;
  # print "Documentation: $outlen\n";
  return $outlen;
}
